def run_bridge_audit():
    logger = AuditLogger()
    if not os.path.exists(logger.report_path):
        print("❌ Error: Audit list (CSV) not found. Run main.py first!")
        return

    # Initialize the real engine
    engine = RealInferenceEngine()
    
    # Load the audit list generated by main.py
    df = pd.read_csv(logger.report_path)
    total_samples = len(df)
    
    print(f"🚀 Starting Real-World Audit: {total_samples} samples to process.")
    start_time = time.time()

    for index, row in df.iterrows():
        # Construct absolute image path
        # Note: image_path in CSV is relative to 'data/audit_payload'
        full_img_path = os.path.join("data/audit_payload", row['Image_Path'])
        
        if not os.path.exists(full_img_path):
            print(f"⚠️ Warning: Image missing at {full_img_path}")
            continue

        # --- EXECUTE REAL INFERENCE ---
        conf = engine.predict(full_img_path)
        status = "PASS" if conf >= 0.8 else "FAIL" # Set your company's threshold
        
        # Update the logger
        logger.update_result(row['Image_Name'], conf, status)
        
        # Progress Tracking
        if (index + 1) % 10 == 0:
            print(f"Progress: [{index + 1}/{total_samples}] samples audited...")

    duration = time.time() - start_time
    print(f"✅ Audit Complete! Time taken: {duration:.2f}s")
    print(f"📊 Report ready at: {logger.report_path}")

if __name__ == "__main__":
    run_bridge_audit()
